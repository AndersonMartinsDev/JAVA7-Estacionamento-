    public void geraTicket() {
        DateFormat forma = DateFormat.getDateInstance();
        DateFormat forma1 = DateFormat.getTimeInstance();
        String servicoNome = cb_serv.getSelectedItem();
        //status.setText("Liberado");
        //Gerando ticket novo aqui 
        Ticket passe = new Ticket();
        passe.setaHoraAtual();
        passe.setaHoraSaida(qualServico(servicoNome));
        lb_codigo.setText(String.valueOf(passe.gerarCodigo()));
        lb_dataEntrada.setText(String.valueOf(forma.format(passe.getEntrada())));
        lb_HoraEntrada.setText(String.valueOf(forma1.format(passe.getEntrada())));
        lb_dataSaida.setText(String.valueOf(forma.format(passe.getSaida())));
        lb_horaSaida.setText(String.valueOf(forma1.format(passe.getSaida())));
        //Aqui será separado para mostrar o serviço que está atrelado ao ticket por isso deve se salvar e mostrar a tolerancia 
        // e o serviço 
        lb_tolerancia.setText(String.valueOf(forma.format(qualServico(servicoNome))));
        banco.salvaTicket(passe);
        System.err.println(passe.getId());
    }

