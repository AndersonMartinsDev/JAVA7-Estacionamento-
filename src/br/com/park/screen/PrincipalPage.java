    public int qualServico(String nomeServ) {
        int tempoTolerancia = 0;
        for (int i = 0; i < banco.getBdServico().size(); i++) {
            if (nomeServ.equalsIgnoreCase(banco.getBdServico().get(i).getNome())) {
                tempoTolerancia = banco.getBdServico().get(i).getTolerancia();
            }
        }
        return tempoTolerancia;
    }

    public final void populaServico() {
        cb_serv.removeAll();
        if (banco.getBdServico().isEmpty()) {
            cb_serv.addItem("Não possui");
        } else {
            for (int x = 0; x < banco.getBdServico().size(); x++) {
                String nomeServ = banco.getBdServico().get(x).getNome();
                cb_serv.add(nomeServ);
            }
        }
    }

    public final void populaConvenio() {
        cb_convenio.removeAll();
        if (banco.getBdConvenio().isEmpty()) {
            cb_convenio.addItem("Não possui");
        } else {
            for (int x = 0; x < banco.getBdConvenio().size(); x++) {
                String nomeServ = banco.getBdConvenio().get(x).getConvenioNome();
                cb_convenio.add(nomeServ);
            }
        }
    }

