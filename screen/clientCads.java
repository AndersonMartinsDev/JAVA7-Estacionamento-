    private void btn_SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SalvarActionPerformed

        Telefone tel = new Telefone();
        Ticket passe = new Ticket();
        try {
            passe.setCodigo(Integer.parseInt(tf_codigo.getText()));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(tf_codigo, "Somente numeros no campo Código", "FORMATO CÓDIGO ERROR ", JOptionPane.ERROR_MESSAGE);

        }
        passe.setaHoraAtual();
        int tt = qualServico(cb_serv.getSelectedItem().toString());
        passe.setaHoraSaida(tt);
        tel.setTelefone(tf_clienteTelefone.getText());
        clienteMensalista cliente = new clienteMensalista(tel, passe);
        cliente.setNome(tf_clienteNome.getText());
        cliente.setEmail(tf_clienteEmail.getText());
        cliente.setCpf(tf_clienteCPF.getText());
        banco.salvaCliente(cliente);
        btnHabilita();
        popTab();
        limpaTela();

        JOptionPane.showMessageDialog(pn_cad, "Salvo com sucesso!");
    }//GEN-LAST:event_btn_SalvarActionPerformed

