    public void btnHabilita() {
        if (!tf_clienteNome.getText().equalsIgnoreCase("")
                || !tf_clienteCPF.getText().equalsIgnoreCase("")) {
            btn_Salvar.setEnabled(false);
        } else {
            btn_Salvar.setEnabled(true);
        }
    }

    public int qualServico(String nomeServ) {
        int tempoTolerancia = 0;
        for (Servicos servico : banco.getBdServico()) {
            if (nomeServ.equalsIgnoreCase(servico.getNome())) {
                tempoTolerancia = servico.getTolerancia();
            }
        }
        return tempoTolerancia;
    }

    public final void populaServico() {
        cb_serv.removeAll();
        if (banco.getBdServico().isEmpty()) {
            cb_serv.addItem("Não possui");
        } else {
            for (Servicos servico : banco.getBdServico()) {
                cb_serv.add(servico.getNome());
            }
        }
    }

    public void limpaTela() {
        tf_clienteCPF.setText("");
        tf_clienteEmail.setText("");
        tf_clienteNome.setText("");
        tf_clienteNome.setText("");
    }

    public void popTab() {

        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"ID", "Nome", "CPF", "Telefone", "E-mail"};
        try {
            for (clienteMensalista cliente : banco.getBdCliente()) {
                int id = cliente.getId();
                String nome = cliente.getNome();
                String cpf = cliente.getCpf();
                String telefone = cliente.getTelefone().getTelefone();
                String email = cliente.getEmail();
                dados.add(new Object[]{id, nome, cpf, telefone, email});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro Ao preencher" + e);
        }

        MinhaTabela Mtable = new MinhaTabela(dados, colunas);
        tb_cadCliente.setModel(Mtable);
        tb_cadCliente.getColumnModel().getColumn(0).setPreferredWidth(20);
        tb_cadCliente.getColumnModel().getColumn(0).setResizable(false);
        tb_cadCliente.getColumnModel().getColumn(1).setPreferredWidth(200);
        tb_cadCliente.getColumnModel().getColumn(1).setResizable(false);
        tb_cadCliente.getColumnModel().getColumn(2).setPreferredWidth(90);
        tb_cadCliente.getColumnModel().getColumn(2).setResizable(false);
        tb_cadCliente.getColumnModel().getColumn(3).setPreferredWidth(90);
        tb_cadCliente.getColumnModel().getColumn(3).setResizable(false);
        tb_cadCliente.getColumnModel().getColumn(4).setPreferredWidth(108);
        tb_cadCliente.getColumnModel().getColumn(4).setResizable(false);

        tb_cadCliente.getTableHeader().setReorderingAllowed(false);
        tb_cadCliente.setAutoResizeMode(tb_cadCliente.AUTO_RESIZE_OFF);
        tb_cadCliente.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        tb_cadCliente.setAlignmentY(RIGHT_ALIGNMENT);
    }

